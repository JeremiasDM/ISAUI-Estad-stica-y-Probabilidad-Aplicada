"""""
La media es simplemente el promedio de los datos. 
Se calcula sumando todos los valores y luego dividiendo por el número de valores.
"""""

def calcular_media(data):
    return sum(data) / len(data) 

"""""
-sum(data): Calcula la suma total de todos los elementos en la lista data.
-len(data): Retorna el número de elementos en la lista data.
-La división sum(data) / len(data) calcula el promedio (media) de los elementos en la lista.
"""""
